    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        //botao adicionar
        DefaultTableModel modelo = (DefaultTableModel) jTable1_produtos.getModel();
        DefaultTableModel modelo2 = (DefaultTableModel) jTable2.getModel();

        itensPedidoDAO idao = new itensPedidoDAO();

        int n = idao.numero();
        int q = Integer.parseInt(JOptionPane.showInputDialog("Qual a quantidade do produto?"));

        itensPedido ip = new itensPedido();

        ip.setPedido(n);
        ip.setDescricao((String) modelo.getValueAt(jTable1_produtos.getSelectedRow(), 1));
        ip.setPreco((Double) modelo.getValueAt(jTable1_produtos.getSelectedRow(), 3));
        ip.setQuantidade(q);

        idao.create(ip);

        while (jTable1_produtos.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        while (jTable2.getRowCount() > 0) {
            modelo2.removeRow(0);
        }

        readJTable();

    }//GEN-LAST:event_jButton4ActionPerformed

