    //Função para ler valores do BD e atualizá-los.
    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1_produtos.getModel();

        ProdutoDAO pDAO = new ProdutoDAO();

        for (Produto p : pDAO.read()) {

            modelo.addRow(new Object[]{
                p.getId_produto(),
                p.getDescricao(),
                p.getMarca(),
                p.getPreco_unitario()

            });

        }

        DefaultTableModel modelo2 = (DefaultTableModel) jTable2.getModel();

        itensPedidoDAO idao = new itensPedidoDAO();

        int n = idao.numero();

        Double total = 0.0;

        for (itensPedido i : idao.carrinho(n)) {

            modelo2.addRow(new Object[]{
                i.getQuantidade(),
                i.getDescricao(),
                i.getPreco() * i.getQuantidade()

            });

            total += i.getPreco() * i.getQuantidade();

        }

        String tot = Double.toString(total);
        jLabel4.setText(tot);

    }

