    public void AtualizaTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        PedidoDAO pDAO = new PedidoDAO();

        for (Pedido p : pDAO.read()) {

            modelo.addRow(new Object[]{
                p.getData_pedido(),
                p.getPessoa().getId_pessoa(),
                pDAO.ProcuraNome(p),
                p.getNumero_pedido(),
                p.getNF(),});
        }
    }

